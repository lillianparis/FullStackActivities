// Here is an else statement
// Here I have set Timmy's age
const age = 18;
// If Timmy's age is greater than or equal to 18, Timmy is an adult
if (age >= 18) {
  console.log("Timmy is an adult.");
  // Here is something new!
  // If the 'if' statement is not true then our 'else' sstatement says Timmy is a child
} else {
  console.log("Timmy is a child.");
}





